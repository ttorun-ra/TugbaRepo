package day7;

public class EscapeCharacters {
    public static void main(String[] args) {
        // Escape characters
        // symbol \
        System.out.println("symbol \\");

        //symbol \t, tabulation, with four spaces
        System.out.println("\t tabulation");

        // symbol \n, new line
        System.out.println("Name: \n Ali");


        System.out.println("What is your Surname: \n Adams");

    }
}


